    private void jSpinner1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner1StateChanged
        if ((int)jSpinner1.getValue()>currentItem.number){
            JOptionPane.showMessageDialog(null, "number of quantity cannot be  higher than our stock number");
            jSpinner1.setValue(currentItem.number);
        }
        else if((int)jSpinner1.getValue()<1){
            JOptionPane.showMessageDialog(null, "number of quantity cannot be 0 or less than 0");
            jSpinner1.setValue(1);
        }
        else
        jTextField12.setText((int)jSpinner1.getValue()*currentItem.cost+"");
    }//GEN-LAST:event_jSpinner1StateChanged
    
